// Build a unified env file for the eval scripts (RAG + agent harness).
//
// The eval harnesses need:
//   - TURSO_DATABASE_URL + TURSO_AUTH_TOKEN  (from .env.turso-prod.local)
//   - KNOSI_SECRET_KEY                       (decrypts ai_providers.api_key_enc;
//                                            production value lives in k3s
//                                            secret knosi-env on Hetzner)
//
// Why this script exists: previously you had to hand-merge these three values
// every session. Easy to forget the secret key and get a confusing
// "Failed to decrypt API key" error.
//
// Output: eval/.eval-env (gitignored). Run: `pnpm eval:prepare`.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { execSync } from "node:child_process";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");

function read(file) {
  const path = resolve(repoRoot, file);
  if (!existsSync(path)) return null;
  return readFileSync(path, "utf8");
}

function extract(text, key) {
  const match = text?.match(new RegExp(`^${key}=(.+)$`, "m"));
  return match ? match[1].trim() : null;
}

const tursoEnv = read(".env.turso-prod.local");
if (!tursoEnv) {
  console.error("Missing .env.turso-prod.local — see .claude/rules/production-turso.md.");
  process.exit(1);
}

const tursoUrl = extract(tursoEnv, "TURSO_DATABASE_URL");
const tursoToken = extract(tursoEnv, "TURSO_AUTH_TOKEN");
if (!tursoUrl || !tursoToken) {
  console.error("Could not parse TURSO_DATABASE_URL / TURSO_AUTH_TOKEN.");
  process.exit(1);
}

// Try local .env first (cheap), fall back to k3s secret on Hetzner (slow).
let secretKey = null;
const localEnv = read(".env.local");
secretKey = extract(localEnv ?? "", "KNOSI_SECRET_KEY");

if (!secretKey || secretKey === "3668bcc3022292dd3f399bc7dbf6d8870aad6bcfbf0212b36b5206451c276263") {
  // The local dev key won't decrypt prod-encrypted API keys. Fetch the prod
  // key from k3s.
  console.log("[prepare-env] Fetching KNOSI_SECRET_KEY from Hetzner k3s …");
  try {
    secretKey = execSync(
      `ssh knosi 'kubectl get secret -n knosi knosi-env -o jsonpath="{.data.KNOSI_SECRET_KEY}" | base64 -d'`,
      { encoding: "utf8" },
    ).trim();
  } catch (err) {
    console.error(
      "Failed to ssh knosi for the prod secret key. " +
        "Either fix your ssh config (Host knosi) or paste the value " +
        "into eval/.eval-env manually.",
    );
    console.error(err.message ?? err);
    process.exit(1);
  }
}

if (!secretKey || secretKey.length < 32) {
  console.error("KNOSI_SECRET_KEY is missing or too short.");
  process.exit(1);
}

const out = `# Auto-generated by scripts/eval-prepare-env.mjs — do not edit.
# Sources: .env.turso-prod.local + Hetzner k3s secret knosi-env.
TURSO_DATABASE_URL=${tursoUrl}
TURSO_AUTH_TOKEN=${tursoToken}
KNOSI_SECRET_KEY=${secretKey}
`;
const outPath = resolve(repoRoot, "eval/.eval-env");
writeFileSync(outPath, out, { mode: 0o600 });
console.log(`[prepare-env] Wrote ${outPath} (chmod 600).`);
console.log(`[prepare-env] Now run:  pnpm eval:rag   or   pnpm eval:agent`);
